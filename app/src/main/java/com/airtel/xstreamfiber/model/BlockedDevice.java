package com.airtel.xstreamfiber.model;

public class BlockedDevice {

    private String mac_address, device_name,  permanent_disabled, time_limit, start_time, end_time;

    public String getDevice_name() {
        return device_name;
    }

    public void setDevice_name(String device_name) {
        this.device_name = device_name;
    }

    public String getMac_address() {
        return mac_address;
    }

    public void setMac_address(String mac_address) {
        this.mac_address = mac_address;
    }

    public String getPermanent_disabled() {
        return permanent_disabled;
    }

    public void setPermanent_disabled(String permanent_disabled) {
        this.permanent_disabled = permanent_disabled;
    }

    public String getTime_limit() {
        return time_limit;
    }

    public void setTime_limit(String time_limit) {
        this.time_limit = time_limit;
    }

    public String getStart_time() {
        return start_time;
    }

    public void setStart_time(String start_time) {
        this.start_time = start_time;
    }

    public String getEnd_time() {
        return end_time;
    }

    public void setEnd_time(String end_time) {
        this.end_time = end_time;
    }
}
